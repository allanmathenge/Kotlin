fun main() {
    println("Hello World!")

    var a: Int = 10
    var b: Int = 5

    // mathematical
    println(a+b)
    println(a-b)
    println(a*b)
    println(a/b)

    println(a%b)

    // Relational
    println(a>b)
    println(a<b)
    println(a>=b)
    println(a<=b)
    println(a==b)
    println(a != 5)

    var x: Int = 20
    println(x--)
    println(--x)
    println(x)
}

